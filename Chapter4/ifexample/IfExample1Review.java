package ifexample;

public class IfExample1Review {

	public static void main(String[] args) {
		// 1분복습  gender 변수 값이 F인경우 여성입니다라고 출력하고 그렇지 않은 경우 남성입니다를 출력하도록 코드를 완성해보기
		
		char gender = 'F';
		
		if(gender == 'F') {
			System.out.println("여성입니다.");
		}
		else {
			System.out.println("남성입니다.");
		}

	}

}
